// Java Program to show the use of
// Arithmetic Operators
import java.io.*;

class Prog5
{
    public static void main (String[] args)
    {
        int a=16, b=23,c=45,d=56;
        System.out.println("a:"+a+" "+"b:"+b+" "+"c:"+c+" "+"d:"+d);
        System.out.println("The sum of a and b :"+(a+b));
        System.out.println("The sum of b and c :"+(b+c));
        System.out.println("The sum of c and d :"+(c+d));
        System.out.println("The difference of d and a :"+(d-a));
        System.out.println("The difference of c and b :"+(c-b));
        System.out.println("The difference of b and a :"+(b-a));




    }
}